// P5715【深基3.例8】三位数排序
// 题目描述
// 给出三个整数 a,b,c(0 <= a,b,c <= 100)，要求把这三位整数从小到大排序。
// 输入格式
// 输入三个整数 a,b,c，以空格隔开。
// 输出格式
// 输出一行，三个整数，表示从小到大排序后的结果。
// 样例输入1
// 1 14 5
// 样例输出1
// 1 5 14
// 样例输入2
// 2 2 2
// 样例输出2
// 2 2 2
#include <stdio.h>
#include <algorithm>

int main() {
    int a[3];
    scanf("%d%d%d", &a[0],&a[1], &a[2]);
    std::sort(a, a+3);
    printf("%d %d %d",a[0], a[1], a[2]);
    return 0;
}