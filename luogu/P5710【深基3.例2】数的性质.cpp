// P5170【深基3.例2】数的性质
// 题目描述
// 一些整数可能拥有以下的性质：
// - 性质 1：是偶数；
// - 性质 2：大于 $4$ 且不大于 $12$。
// 小 A 喜欢这两个性质同时成立的整数；Uim 喜欢这至少符合其中一种性质的整数；八尾勇喜欢刚好有符合其中一个性质的整数；正妹喜欢不符合这两个性质的整数。现在给出一个整数 $x$，请问他们是否喜欢这个整数？
// 输入格式
// 输入一个整数 $x(0\le x \le 1000)$
// 输出格式
// 输出这 $4$ 个人是否喜欢这个数字，如果喜欢则输出 1，否则输出 0，用空格分隔。输出顺序为：小 A、Uim、八尾勇、正妹。
// 样例输入
// 12
// 样例输出
// 1 1 0 0
#include <stdio.h>

int main() {
    // 读取输入整数
    int x;
    scanf("%d", &x);

    // 判断四个性质
    int property1 = (x % 2 == 0);      // 性质1：偶数
    int property2 = (x > 4 && x <= 12); // 性质2：大于4且不大于12

    // 输出结果
    printf("%d %d %d %d\n", property1, property2, property1 && !property2, !property1 && !property2);

    return 0;
}