const int n = 30, m  = 50;
int res = 0x7ffffff;
int book[n][m];

struct node {
    int x;
    int y;
    int s; // 路程
    int f; // 上一个位置下标
    char ch; // 上一个走向
};
node que[n * m];
int head, tail;
// 按字典绪从大到小顺序排列
int next_[4][2] = {
    {1, 0}, // 下
    {0, -1}, // 左
    {0, 1}, // 右
    {-1, 0} // 上
};

void print(int index) {
    if(index == 0) {
        return ;
    }
    print(que[index].f);
    printf("%s", &que[index].ch);
}

void bfs() {
    que[tail].x = 0;
    que[tail].y = 0;
    que[tail].s = 0;
    que[tail].f = -1;
    book[0][0] = 1;
    int flag = 0;

    while(head < tail) {
        int nX, nY;
        for(int i = 0; i < 4; ++ i) {
            nX = next_[i][0] + que[head].x;
            nY = next_[i][1] + que[head].y;
            if(nX < 0 || nX >= n || nY < 0 || nY >= m) {
                continue;
            }
            if(map[nX][nY] == 0 && book[nX][nY] == 0) {
                book[nX][nY] = 1;
                que[tail].x = nX;
                que[tail].y = nY;
                que[tail].s = que[head].s + 1;
                que[tail].f = head;
                if(next_[i][0]) {
                    que[tail].ch = (next_[i][0] == 1 ? 'D' : 'U');
                }else if(next_[i][1]) {
                    que[tail].ch = (next_[i][1] == 1 ? 'R' : 'L');
                }
                tail ++;
                // 找到出口
                if(nX == n - 1 && nY == m - 1) {
                    flag = 1;
                    break;
                }
            }
        }
        if(flag == 1) {
            break;
        }
        head ++;
    }
    print(tail - 1);
}

int main() {
    bfs();
    return 0;
}

